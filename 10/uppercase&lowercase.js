// uppercase and lowercase

let str = "Hello World, Welcome To JavaScript!";

console.log(str.toUpperCase()); // HELLO WORLD, WELCOME TO JAVASCRIPT!

str = str.toUpperCase();
console.log(str); // HELLO WORLD, WELCOME TO JAVASCRIPT!

str = str.toLowerCase();
console.log(str); // hello world, welcome to javascript!

console.log(str.toLowerCase()); // hello world, welcome to javascript!
